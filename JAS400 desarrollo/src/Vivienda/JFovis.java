    public void get_Periodos_Fovis(){
          try {
            String Str_Sql = "SELECT calper FROM vivienda.finspos " +
                             " GROUP BY CALPER "+
                             " ORDER BY 1 desc ";
            ResultSet rs = JBase_Datos.SQL_QRY(this.Cn,Str_Sql);
            jComboBox1.addItem("");
            while(rs.next()){
                jComboBox1.addItem(""+rs.getString("calper").trim());   
            }
            
          } catch (Exception e) {
            JOptionPane.showMessageDialog(this,"JFovis: get_Periodos_Fovis() "+e.getMessage()); 
          }
    } 
    
    public void Mostrar_Fovis(){
        try {
            String Str_Sql = "select * from selinlib.ZPUNTUACI6 ";
            
            ResultSet rs = JBase_Datos.SQL_QRY(this.Cn,Str_Sql);
            Cabecera[0]= "Diferencia";
            Cabecera[1]= "Sistema Fovis";
            Cabecera[2]= "Auditoria";
            Cabecera[3]= "B1";
            Cabecera[4]= "B2";
            Cabecera[5]= "B3";
            Cabecera[6]= "B4";
            Cabecera[7]= "B5";
            Cabecera[8]= "B6";
            Cabecera[9]= "Documento";
            Cabecera[10]= "Nombre";
            int fila = -1;
            while(rs.next()){
                fila++;
                this.Detalle [fila][0] = this.JFormato.format(rs.getDouble("DIFEREN"));
                this.Detalle [fila][1] = this.JFormato.format(rs.getDouble("PTFOVIS"));
                this.Detalle [fila][2] = this.JFormato.format(rs.getDouble("PUNTAJE"));
                this.Detalle [fila][3] = this.JFormato.format(rs.getDouble("B1"));
                this.Detalle [fila][4] = this.JFormato.format(rs.getDouble("B2"));
                this.Detalle [fila][5] = this.JFormato.format(rs.getDouble("B3"));
                this.Detalle [fila][6] = this.JFormato.format(rs.getDouble("B4"));
                this.Detalle [fila][7] = this.JFormato.format(rs.getDouble("B5"));
                this.Detalle [fila][8] = this.JFormato.format(rs.getDouble("B6"));
                this.Detalle [fila][9] = this.JFormato.format(rs.getDouble("DOCUMENT"));
                this.Detalle [fila][10] = rs.getString("NOMBRE").trim();
                
            }
            jTable1.setModel(new javax.swing.table.DefaultTableModel(Detalle, Cabecera));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,"JFovis: Mostrar_Fovis "+e.getMessage()); 
        }
    }
    
