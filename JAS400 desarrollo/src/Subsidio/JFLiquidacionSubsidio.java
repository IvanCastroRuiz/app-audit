    public void getInformacion(){
        if (this.Subsidio.getExiste()) {
             this.Cabecera [0]= "Numero del Cheque";
             this.Cabecera [1]= "Fecha";
             this.Cabecera [2]= "Periodo";
             this.Cabecera [3]= "Concepto";
             this.Cabecera [4]= "Descripcion del Concepto";
             this.Cabecera [5]= "Documento del Afiliado";
             this.Cabecera [6]= "Documento del Beneficiario";
             this.Cabecera [7]= "Nombre del Beneficiario";
             this.Cabecera [8]= "Valor";
             int fila = -1;
             this.Subsidio.setFirst();
             while(this.Subsidio.getNext()){
                fila++;
                this.Detalle [fila][0] =""+JFormato.format(Double.parseDouble(this.Subsidio.getNumeroCheque()));
                this.Detalle [fila][1] =""+this.Subsidio.getFecha();
                this.Detalle [fila][2] =""+this.Subsidio.getPeriodo();
                this.Detalle [fila][3] =""+this.Subsidio.getConcepto();
                this.Detalle [fila][4] ="";
                this.Detalle [fila][5] =""+JFormato.format(Double.parseDouble(this.Subsidio.getDocumentoTrabajador()));
                this.Detalle [fila][6] =""+JFormato.format(Double.parseDouble(this.Subsidio.getDocumentoBenefiaciado()));
                this.Detalle [fila][7] =""+this.Subsidio.getNombreBeneficiario();
                this.Detalle [fila][8] =""+JFormato.format(Double.parseDouble(this.Subsidio.getValor()));
            }
            jTable1.setModel(new javax.swing.table.DefaultTableModel(Detalle, Cabecera));
        }
        
    }
