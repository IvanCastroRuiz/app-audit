    private void setPeriodos(){
       try{
            String Str_Sql ="select distinct perio from AUXLIB.MAUXIL order by 1 desc ";
            ResultSet xResult =  JBase_Datos.SQL_QRY(this.Cn,Str_Sql);
            while(xResult.next()){
                String xResponsable = xResult.getString("perio");
                jCPeriodo.addItem(xResponsable);
            }
       }catch(Exception e ){
           JOptionPane.showMessageDialog(this,"Error "+e.getMessage());
       }
    }
