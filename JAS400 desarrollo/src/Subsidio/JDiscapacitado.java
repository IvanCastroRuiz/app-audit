    /*
        Obtiene la informacion de los beneficiarios del subsdio sea por periodo o por cedula o por ambos casos.
        @param cedula: cedula del trabajador beneficiado del subsidi escolar de discapacidad
               periodo: periodo de analisis
        @return vacio
    */
    public void getInformacion(String cedula , String periodo){
        try {
            String Str_Sql ="";
            if(cedula.trim().equals("") || cedula.trim().equals("*all")){
                Str_Sql ="  select  BEDRAD, CODCOL,AFICAT,CEDNIT,CEDDES , t01.*             \n" +
                            "          from becaslib.mbecasm as t01 , subsilib.mcenedu as t02 \n" +
                            " where CODCOL = CEDCOD  and permon ="+periodo;
            }else{
                if (periodo.trim().equals("") || periodo.trim().equals("*all")){
                    Str_Sql ="  select  BEDRAD, CODCOL,AFICAT,CEDNIT,CEDDES , t01.*             \n" +
                            "          from becaslib.mbecasm as t01 , subsilib.mcenedu as t02 \n" +
                            " where CODCOL = CEDCOD  and cedtra = "+cedula;
                }else{
                    Str_Sql ="  select  BEDRAD, CODCOL,AFICAT,CEDNIT,CEDDES , t01.*             \n" +
                            "          from becaslib.mbecasm as t01 , subsilib.mcenedu as t02 \n" +
                            " where CODCOL = CEDCOD  and cedtra = "+cedula+" and permon ="+periodo;
                }             
                
            }
             
             ResultSet rs = JBase_Datos.SQL_QRY(this.Cn,Str_Sql);
             
             this.setTable(this.JTable ,rs);
            //Ajustar las columnas de forma automatica 
            JTable.setAutoResizeMode(JTable.AUTO_RESIZE_OFF);
            JTable.setAutoCreateRowSorter(true);
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "setTable(javax.swing.JTable jtQuery , ResultSet rs) "+e.getMessage());
        }
    }
    /*
        @param comparativo de pago de subsidio discapacitados por dos periodos .
        @periodo_1: primer periodo de comparacion.
        @periodo_2: segundo periodo de comparacio.
    */
public void getInformacion_comparativo(String periodo_1 , String periodo_2){
        try {
            String Str_Sql =" select t1.cednit ,t2.ceddes , pagado_"+periodo_1+" , pagado_"+periodo_2+"  , cantidad_"+periodo_1+" , cantidad_" +periodo_2+
                            "         from (                                                     \n" +
                            "select  permon , cednit,ceddes , sum(valpag) pagado_"+periodo_1+"  , count(*) cantidad_"+periodo_1+
                            "         from becaslib.mbecasm as t01 , subsilib.mcenedu as t02     \n" +
                            "where CODCOL = CEDCOD  and  permon =" +periodo_1+
                            " group by permon , cednit , ceddes                                   \n" +
                            "order by cednit ) as t1 left join                                   \n" +
                            "(select permon , cednit,ceddes , sum(valpag) as pagado_"+periodo_2+",count(*) cantidad_"+periodo_2 +
                            "          from becaslib.mbecasm as t01 , subsilib.mcenedu as t02    \n" +
                            " where CODCOL = CEDCOD  and  permon =" +periodo_2+
                            " group by permon , cednit , ceddes                                  \n" +
                            " order by cednit ) as t2 on t1.cednit =t2.cednit                    ";
             ResultSet rs = JBase_Datos.SQL_QRY(this.Cn,Str_Sql);
             
             this.setTable(this.JTable1 ,rs);
            //Ajustar las columnas de forma automatica 
            JTable1.setAutoResizeMode(JTable1.AUTO_RESIZE_OFF);
            JTable1.setAutoCreateRowSorter(true);
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "setTable(javax.swing.JTable jtQuery , ResultSet rs) "+e.getMessage());
        }
    }



public void setTable(javax.swing.JTable jtQuery , ResultSet rs){
        /*
           @param jtQuery tabla que contendra informacion del query ejecutado
           @param rs variable que contendra el resulselt del Sql ejectuado
        */
        
        try {

           DefaultTableModel modelo = new DefaultTableModel();
           jtQuery.setModel(modelo); 
            
           ResultSetMetaData rsMd = rs.getMetaData();
           int cantidadColumnas = rsMd.getColumnCount();
 
           for (int i = 1; i <= cantidadColumnas; i++) {
                modelo.addColumn(rsMd.getColumnLabel(i));
                
           }
           //Creando las filas para el JTable
            while (rs.next()) {
                Object[] fila = new Object[cantidadColumnas];
                for (int i = 0; i < cantidadColumnas; i++) {
                    fila[i]=rs.getObject(i+1);
                }
                modelo.addRow(fila);
            }
            rs.close();
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "getInformacion_prescripcion() "+e.getMessage());
        }
    }

