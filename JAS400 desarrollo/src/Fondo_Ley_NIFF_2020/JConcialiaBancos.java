     public void setTabla(){
        try {
            String Str_Sql = " select * from selinlib.JFCONCIBAN Where fecmov >="+jTFechaIncial.trim()+" and fecmov<= "+jTFechaFinal.trim();
            ResultSet Rs = JBase_Datos.SQL_QRY(this.Cn,Str_Sql);
            String Ctamov, Docmov, Nummov;
            while(Rs.next()){
                this.Detalle = new Vector();
                Ctamov = Rs.getString("ctamov");
                this.Detalle.add(Ctamov);
                this.Detalle.add(Rs.getString("descrip"));
                this.Detalle.add(this.JFormato.format(Rs.getDouble("debmov")));
                this.Detalle.add(this.JFormato.format(Rs.getDouble("cremov")));
                String PFecha = Rs.getString("fecmov");
                this.Detalle.add(PFecha);
                Docmov = Rs.getString("docmov");
                this.Detalle.add(Docmov);
                Nummov= Rs.getString("nummov");
                this.Detalle.add(Nummov);
                getComplemento_Cuenta(Docmov, Nummov, Ctamov,PFecha);
                this.Detalle.add(Ctamov2);
                this.Detalle.add(Credmov2);
                this.Detalle.add(Debmov2);
                this.Fila.add(Detalle);
            }
            Rs.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this," setTabla()"+e.getMessage() );
        }
        this.jTable1.setModel(new javax.swing.table.DefaultTableModel(this.Fila, this.Cabecera));
    }
    
