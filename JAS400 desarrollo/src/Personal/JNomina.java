    public void setLiquidacionNomina(){
        double BSalario = 0, HorasLaborales = 0, HorasExtrasDiurnas = 0, HorasExtrasNocturnas = 0, HorasRecargoNocturno = 0;
        double HorasExtrasDominicalesDiurna = 0, HorasExtrasDominicalesNocturna = 0, RecargoDominicalDiurno = 0, RecargoDominicalNocturna = 0;
        try {
            
            BSalario =  Double.parseDouble(jTextField1.getText());
            HorasLaborales = Double.parseDouble(jTextField2.getText());
            HorasExtrasDiurnas = Double.parseDouble(jTextField3.getText());
            HorasExtrasNocturnas = Double.parseDouble(jTextField4.getText());
            HorasRecargoNocturno = Double.parseDouble(jTextField5.getText());
            
            HorasExtrasDominicalesDiurna = Double.parseDouble(jTextField6.getText());
            HorasExtrasDominicalesNocturna = Double.parseDouble(jTextField7.getText());
            RecargoDominicalDiurno = Double.parseDouble(jTextField8.getText());
            RecargoDominicalNocturna = Double.parseDouble(jTextField9.getText());
            JLiquidacionNomina lq = new JLiquidacionNomina(JBase_Datos , Connection_BD,0, BSalario);
            //Horas laborada durante una semana normal
            lq.setHorasLaborales(HorasLaborales);
            lq.setHorasExtrasDiurnas(HorasExtrasDiurnas);
            lq.setHorasExtrasNocturnas(HorasExtrasNocturnas);
            lq.setHorasRecargoNocturno(HorasRecargoNocturno);
            //Horas laborada durante fines de semana
            lq.setHorasExtrasDiurna_Domingo_Festivo(HorasExtrasDominicalesDiurna);
            lq.setHorasExtrasNocturno_Domingo_Festivo(HorasExtrasDominicalesNocturna);
            lq.setHoras_Recargo_Diurno_Domingo_Festivo(RecargoDominicalDiurno);
            lq.setHoras_Recargo_Nocturno_Domingo_Festivo(RecargoDominicalNocturna);
            
            lq.setLiquidacionNomina();
            jTextField13.setText(""+JFormato.format(lq.getNetoaPagar()));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,"Class: JNomina : JControl "+e.getMessage());
        }
        
    }
    
    
