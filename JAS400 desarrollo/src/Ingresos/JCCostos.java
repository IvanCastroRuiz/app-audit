    public void setInformacion_Table(){
        try {
            Fila.clear();
            String Str_Sql ="select * from selinlib.JCCENTROS ";
            ResultSet rs = JBase_Datos.SQL_QRY(this.Cn,Str_Sql);
            while(rs.next()){
                 Det = new Vector();
                 Det.add(rs.getString("CAC"));
                 Det.add(rs.getString("CODIGO"));
                 Det.add(rs.getString("DESCRIP"));
                 Det.add(rs.getString("CODCONT"));
                 Det.add(rs.getString("CODTARJ"));
                 Fila.add(Det);
             }
             this.jTable1.setModel(new javax.swing.table.DefaultTableModel(Fila, Cab));
             rs.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,"Class:JCCostos:setInformacion_Table()"+e.getMessage()); 
        }
    }
    
    
    
