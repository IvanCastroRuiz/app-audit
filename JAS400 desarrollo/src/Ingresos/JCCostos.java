    public void Delete_CAC(){
         try {
                if (jTable1.getSelectedRow()!= -1){
                   String Str_Sql ="DELETE FROM SELINLIB.JCCENTROS  WHERE CAC="+jTable1.getValueAt(jTable1.getSelectedRow(),0).toString();
                   boolean SQL = JBase_Datos.Exc_Sql(this.Cn,Str_Sql);
                   if(SQL){
                        setInformacion_Table();
                        JOptionPane.showMessageDialog(this," Se eleminio correctamente ");   
                   }
                }else{
                    JOptionPane.showMessageDialog(this," Debe seleccionar un registro en la tabla ");
                }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this," Delete_CAC() " +e.getMessage());
        }
    }
    public void Guardar_Centro_Costos(String PCac,String PCodigo, String PDescrip, String PCod_Contable, String PCod_tarjetas ){
        try {
            String Str_Sql ="select * from selinlib.jccentros where cac="+PCac;
            ResultSet rs = JBase_Datos.SQL_QRY(this.Cn,Str_Sql);
            if (rs.next()){
                Str_Sql ="UPDATE SELINLIB.JCCENTROS SET CODIGO =  "+PCodigo+" ,DESCRIP='"+PDescrip+"' , CODCONT="+PCod_Contable+" , CODTARJ="+PCod_tarjetas+""
                        + " WHERE CAC="+PCac;
            }else{
                Str_Sql= "INSERT INTO SELINLIB.JCCENTROS (CAC,CODIGO,DESCRIP,CODCONT,CODTARJ ) VALUES("+PCac+","+PCodigo+",'"+PDescrip+"',"+PCod_Contable+","+PCod_tarjetas+")";
            }
            //System.out.println(""+Str_Sql);
            boolean SQL = JBase_Datos.Exc_Sql(this.Cn,Str_Sql);
            if(SQL){
                setInformacion_Table();
                JOptionPane.showMessageDialog(this," Se Guardo Correctamente ");
            }
            rs.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this," Guardar_Centro_Costos() "+e.getMessage());
        }
    }
    
    
