    public double get_cambio_efectivo(String Fecha, String Cajero){
        double cambio =0;
        try {
            String Str_Sql ="select sum(VLRCAM)   as valor    \n" +
                            "       from poslib.posv01\n" +
                            "where FECTRA =" +Fecha+
                            "      and CODCJE ="+Cajero;
            ResultSet rs = JBase_Datos.SQL_QRY(this.Cn,Str_Sql);
            if(rs.next()){
                cambio =rs.getDouble("valor");
            }
            rs.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,"get_cambio_efectivo(String Fecha, String Cajero) "+e.getMessage());  
        }
        return cambio;
    }
    public void get_medios_pagos(String Fecha, String caja){
        try {
             String Str_Sql ="select t1.CODPAG , t2.DESPAG  , sum(t1.VLRPAG) as valor     \n" +
                             "         from poslib.POSV03 as t1 ,  poslib.POSMPAG as t2   \n" +
                             "where t1.CODPAG=T2.CODPAG                                   \n" +
                             " and FECTR3 ="+Fecha +
                             " and CODCJ3 =" +caja+
                             " and ESTAD3 = 'P'       "
                            +" group by t1.CODPAG , t2.DESPAG       ";
             ResultSet rs = JBase_Datos.SQL_QRY(this.Cn,Str_Sql);
             this.setTable(this.jTable1 ,rs);
             //Ajustar las columnas de forma automatica 
             jTable1.setAutoResizeMode(JTable.AUTO_RESIZE_OFF);
             jTable1.setAutoCreateRowSorter(true);
            rs.close();
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "setTable(javax.swing.JTable jtQuery , ResultSet rs) "+e.getMessage());
        }
    }
    
    
    public void setTable(javax.swing.JTable jtQuery , ResultSet rs){
        /*
           @param jtQuery tabla que contendra informacion del query ejecutado
           @param rs variable que contendra el resulselt del Sql ejectuado
        */
        
        try {

           DefaultTableModel modelo = new DefaultTableModel();
           jtQuery.setModel(modelo); 
            
           ResultSetMetaData rsMd = rs.getMetaData();
           int cantidadColumnas = rsMd.getColumnCount();
 
           for (int i = 1; i <= cantidadColumnas; i++) {
                modelo.addColumn(rsMd.getColumnLabel(i));
                
           }
           //Creando las filas para el JTable
            while (rs.next()) {
                Object[] fila = new Object[cantidadColumnas];
                for (int i = 0; i < cantidadColumnas; i++) {
                    fila[i]=rs.getObject(i+1);
                }
                modelo.addRow(fila);
            }
            rs.close();
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "getInformacion_prescripcion() "+e.getMessage());
        }
    }
    
    
