    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
       try{
        jTextField1 = d.format(jXDatePicker1.getDate()).toString();
        jTextField2 = d.format(jXDatePicker2.getDate()).toString();
        if((!this.jTextField1.trim().equals("") )&& (!this.jTextField2.trim().equals(""))){
           String Mes1 = jTextField1.trim().substring(4, 6);
           String Mes2 = jTextField2.trim().substring(4, 6);
           if(Mes1.trim().equals(Mes2.trim())){
                JAutomaticoVentasRetenidas VRetenida = new JAutomaticoVentasRetenidas(this.jTextField1.trim(),this.jTextField2.trim(),this.JBase_Datos , this.Connection_BD);
                VRetenida.Genera_Ventas_Automatico();
           }else{
               JOptionPane.showMessageDialog(null," Este proceso Debe ejecutar solo en rango de un Mes Maximo"); 
           }
        }else{
            JOptionPane.showMessageDialog(this, "Debe ingresar una fecha no mayor a un Mes");
        }
       }catch(Exception e ){
            JOptionPane.showMessageDialog(this, "Debe ingresar la fecha "+e.getMessage());
       }
    }//GEN-LAST:event_jButton1ActionPerformed

