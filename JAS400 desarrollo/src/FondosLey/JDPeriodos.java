    public boolean Guardar(String Periodo, String Estado){
        boolean Rs=false;
        try {
            if(!this.getExiste(Periodo)){
                String Str_Sql="insert into SELINLIB.JFDISTRPER VALUES("+Periodo+",'"+Estado+"','"+Usuario+"','"+FechaUsuario+"')";
                Rs = JBase_Datos.Exc_Sql(this.Cn,Str_Sql);
            }else{
                String Str_Sql="update SELINLIB.JFDISTRPER "
                        + " set estado='"+Estado+"'"
                        + " , usuari='"+Usuario+"'"
                        + " , fecusu='"+FechaUsuario+"'"
                        + " where periodo="+Periodo;
                Rs = JBase_Datos.Exc_Sql(this.Cn,Str_Sql);
                
            }
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,"Guardar() "+e.getMessage() );
        }
        return Rs;
    }
    
    
    public boolean getExiste(String Periodo){
        boolean Control = false;
        try {
            String Str_Sql ="select * from selinlib.JFDISTRPER where periodo="+Periodo;
            ResultSet Rs = JBase_Datos.SQL_QRY(this.Cn,Str_Sql);
            if(Rs.next()){
                Control=true;
            }
            Rs.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,"getExiste() "+e.getMessage() );
        }
        return Control;
    }
