    public void getInformacion(){
        try {
            String Str_Sql ="select * from SELINLIB.JFDISTRPER ORDER BY Periodo desc";
            ResultSet Rs = JBase_Datos.SQL_QRY(this.Cn,Str_Sql);
            this.Fila.clear();
            while(Rs.next()){
                this.Detalle = new Vector();
                this.Detalle.add(Rs.getInt("Periodo"));
                this.Detalle.add(Rs.getString("Estado"));
                this.Detalle.add(Rs.getString("Usuari"));
                this.Detalle.add(Rs.getString("Fecusu"));
                this.Fila.add(this.Detalle);
            }
            this.jTable1.setModel(new javax.swing.table.DefaultTableModel(this.Fila, this.Cabecera));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,"getInformacion()" );
        }
    }
    
    
    
    
