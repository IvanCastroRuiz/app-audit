private void jTree1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTree1MouseClicked
// TODO add your handling code here:
  try{  
    if (evt.getClickCount() == 2) {
        //Menu de Planeacion
        if ( jTree1.getSelectionModel().getSelectionPath().toString().trim().equals("[root, Auditoria, Planeacion, Priorizacion]") ){
             JPriorizacion Pr = new JPriorizacion(this.JBase_Datos, this.Connection_BD);
             Pr.setVisible(true);
        }
        if ( jTree1.getSelectionModel().getSelectionPath().toString().trim().equals("[root, Auditoria, Planeacion, Asignacion de Recursos]") ){
            //TipoAsignacion Tp = new TipoAsignacion();
            TipoAsignacion_Por_Mes_Periodo Tp = new TipoAsignacion_Por_Mes_Periodo(this.JBase_Datos, this.Connection_BD, this.Usuario);
            Tp.setExtendedState(javax.swing.JFrame.MAXIMIZED_BOTH);
            Tp.setVisible(true);
        }
        //Menu de Interfaz
        if ( jTree1.getSelectionModel().getSelectionPath().toString().trim().equals("[root, Interfaz, Cargar Mapas de Riesgo]") ){
            JTaller Taller = new JTaller(this.JBase_Datos, this.Connection_BD);
        }
        if ( jTree1.getSelectionModel().getSelectionPath().toString().trim().equals("[root, Auditoria, Monitoreo, Por Recursos/ Por Meses]") ){
           AuditoriaRecursos Ar= new AuditoriaRecursos (this.JBase_Datos, this.Connection_BD);
           Ar.setVisible(true);
        }
        if ( jTree1.getSelectionModel().getSelectionPath().toString().trim().equals("[root, Auditoria, Monitoreo, Seguimiento de Informes]") ){
            JSeguimientos Frm_Seguimiento =  new JSeguimientos(this.JBase_Datos , this.Connection_BD);
            Frm_Seguimiento.setExtendedState(javax.swing.JFrame.MAXIMIZED_BOTH);
            Frm_Seguimiento.setVisible(true);
        }
        if ( jTree1.getSelectionModel().getSelectionPath().toString().trim().equals("[root, Auditoria, Ejecucion, Seleccion]") ){
           InformeSelecion Is= new InformeSelecion (this.JBase_Datos, this.Connection_BD, this.Usuario);
           Is.setBounds(10,10,1200, 600);
           Is.setVisible(true);
        }
        if ( jTree1.getSelectionModel().getSelectionPath().toString().trim().equals("[root, Mapa de Riesgo, Construccion]") ){
            MapaRiesgo Mr = new MapaRiesgo ();
            Mr.setVisible(true);
        }
        if ( jTree1.getSelectionModel().getSelectionPath().toString().trim().equals("[root, Mapa de Riesgo, Seguimiento]") ){
        }
        
        System.out.println(jTree1.getSelectionModel().getSelectionPath().toString().trim());
    }
  }catch(Exception e ){}
}//GEN-LAST:event_jTree1MouseClicked

